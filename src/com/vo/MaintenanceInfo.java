package com.vo;

public class MaintenanceInfo {
	private int main_seq;
	private String change_time;
	private int change_st;
	private String car_num;
	private String part_id;
	private int mile;
	public int getMain_seq() {
		return main_seq;
	}
	public void setMain_seq(int main_seq) {
		this.main_seq = main_seq;
	}
	public String getChange_time() {
		return change_time;
	}
	public void setChange_time(String change_time) {
		this.change_time = change_time;
	}
	public int getChange_st() {
		return change_st;
	}
	public void setChange_st(int change_st) {
		this.change_st = change_st;
	}
	public String getCar_num() {
		return car_num;
	}
	public void setCar_num(String car_num) {
		this.car_num = car_num;
	}
	public String getPart_id() {
		return part_id;
	}
	public void setPart_id(String part_id) {
		this.part_id = part_id;
	}
	public int getMile() {
		return mile;
	}
	public void setMile(int mile) {
		this.mile = mile;
	}
	
}
 